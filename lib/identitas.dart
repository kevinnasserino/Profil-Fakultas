class Identitas {
  final String daftar;
  final String nama;
  final String foto;
  final String ttl;
  final String alamat;
  final String hp;
  final String email;
  final String url;
  final List<String> pendidikan;
  final List<String> penghargaan;
 
  Identitas({
    required this.daftar,
    required this.nama,
    required this.foto,
    required this.ttl,
    required this.alamat,
    required this.hp,
    required this.email,
    required this.url,
    required this.pendidikan,
    required this.penghargaan,
  });
}
  